public class resta {
    
    
		
		
		private int num1;

		private int num2;

		public resta(int n1, int n2) {
		    num1 = n1;
		    num2 = n2;
		   
		}

		public int restar() {
		    int resultado = num1 - num2;
		    return resultado;
		}
    
}
